# Coloque em pratica os is

caracter = input("\n Digite algo: \n")

print("\n O tipo primitivo é: \n", type(caracter))
print("\n È um numero? \n", caracter.isalnum())
print("\n È uma letra? \n", caracter.isalpha())
print("\n È um digito? \n", caracter.isdigit())
print("\n È um espaço? \n", caracter.isspace())
print("\n È um alfanumérico? \n", caracter.isalnum())





